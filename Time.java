public class Time {
    // Attributes

    private int hour = 14;
    private int minute = 34;
    private int second = 26;

    // Constructors

    // Parameterized Constructor
    public Time(int h, int m, int s) {
        this.hour = h;
        this.minute = m;
        this.second = s;
    }

    // Methods

    // Gets the hour
    public int getHour() {
        return this.hour;
    }

    // Gets the minute
    public int getMinute() {
        return this.minute;
    }

    // Gets the second

}
